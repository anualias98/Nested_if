# find vowels & consonants
x=input("Enter your letter =")
if x=="a":
    print("This is the vowel a")
elif x=="e":
    print("This is the vowel e")
elif x=="i":
    print("This is the vowel i")
elif x=="o":
    print("This is the vowel o")
elif x=="u":
    print("This is the vowel u")
else:
    print("This is a consonant")